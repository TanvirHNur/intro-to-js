// +	        Addition	    Adds two numbers together.	                        6 + 9
// -	        Subtraction	    Subtracts the right number from the left.	        20 - 15
// *	        Multiplication	Multiplies two numbers together.	                3 * 7
// /	        Division	    Divides the left number by the right.	            10 / 5
// %	        Remainder       (sometimes called modulo)	                        8 % 3 (returns 2, as three goes into 8 twice, leaving 2 left over).

//             Returns the remainder left over after you've divided the 
//             left number into a number of integer portions equal to the right number.


